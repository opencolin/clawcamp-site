-- ===========================================================================
-- 8. RLS + GRANTS  (anon is a hostile public client — same threat model as
--    0001 §lockdown and 0002 §6)
-- ===========================================================================
-- The anon key ships in the browser bundle and is held by an untrusted client.
-- This section is the deferred `events` lockdown from 0001 plus the child-table
-- write closure. The combined effect:
--   * anon reads ONLY approved events (server-side moderation gate; replaces
--     the leaky client HIDDEN_EVENT_IDS),
--   * anon can NO LONGER POST to /rest/v1/events (writes go through the Edge
--     Function with the service-role key),
--   * anon may READ child rows (the detail page needs them) but can NEVER
--     write them — the non-negotiable "never a raw anon INSERT on new tables".
-- ===========================================================================

-- --- events ---------------------------------------------------------------

-- WHAT: enable RLS on events so the policies below are enforced.
-- WHY:  0001 left events with RLS effectively wide open (anon SELECT+INSERT).
--       This is the deferred lockdown; turning RLS on is step one.
ALTER TABLE public.events ENABLE ROW LEVEL SECURITY;

-- WHAT: anon may SELECT only approved events.
-- WHY:  THIS is the server-side moderation gate. Every anon REST read of
--       events (events/index.html, events/detail, homepage index.html,
--       chapters/index.html — all of them) now returns ONLY approved rows,
--       replacing the cosmetic client-side HIDDEN_EVENT_IDS filter. Non-approved
--       rows (draft/submitted/rejected) are invisible to the hostile client.
CREATE POLICY events_select_approved
  ON public.events
  FOR SELECT
  TO anon
  USING (status = 'approved');

-- WHAT: authenticated users (incl. the service role) may SELECT every event.
-- WHY:  /admin must list submitted/draft/rejected rows to moderate them, and
--       it does so with the admin's USER JWT (the `authenticated` role; the
--       admin allowlist is enforced in the admin page + Edge Function as the
--       interim stopgap — see header). The service role used by the Edge
--       Function also needs full read to return the row it just inserted.
CREATE POLICY events_select_authenticated
  ON public.events
  FOR SELECT
  TO authenticated
  USING (true);

-- WHAT: revoke the blanket anon INSERT on events.
-- WHY:  anon must no longer POST directly to /rest/v1/events (no validation,
--       no moderation, client-settable columns). All event writes now flow
--       through the submit-event Edge Function, which holds the service-role
--       key and forces status='submitted'. This is the assertion target of the
--       rls-probe events write checks.
REVOKE INSERT ON public.events FROM anon;

-- --- event_speakers -------------------------------------------------------

-- WHAT: enable RLS; grant anon SELECT only; public read policy; NO anon write.
-- WHY:  the detail page reads child rows over the anon key, so anon needs
--       SELECT. anon gets NO INSERT/UPDATE/DELETE grant — writes are
--       service-role only via the Edge Function. This is the council rule:
--       "never a raw anon INSERT on new tables." rls-probe (d) asserts the
--       anon write is denied.
ALTER TABLE public.event_speakers ENABLE ROW LEVEL SECURITY;
GRANT SELECT ON public.event_speakers TO anon;
CREATE POLICY event_speakers_select_public
  ON public.event_speakers
  FOR SELECT
  TO anon
  USING (true);

-- --- event_schedule -------------------------------------------------------

-- WHAT: enable RLS; grant anon SELECT only; public read policy; NO anon write.
-- WHY:  same posture as event_speakers — detail page reads, hostile client
--       cannot write. rls-probe (d) asserts the anon write is denied.
ALTER TABLE public.event_schedule ENABLE ROW LEVEL SECURITY;
GRANT SELECT ON public.event_schedule TO anon;
CREATE POLICY event_schedule_select_public
  ON public.event_schedule
  FOR SELECT
  TO anon
  USING (true);

-- --- event_sponsors -------------------------------------------------------

-- WHAT: enable RLS; grant anon SELECT only; public read policy; NO anon write.
-- WHY:  same posture as the other child tables — detail page reads, hostile
--       client cannot write. rls-probe (d) asserts the anon write is denied.
ALTER TABLE public.event_sponsors ENABLE ROW LEVEL SECURITY;
GRANT SELECT ON public.event_sponsors TO anon;
CREATE POLICY event_sponsors_select_public
  ON public.event_sponsors
  FOR SELECT
  TO anon
  USING (true);
