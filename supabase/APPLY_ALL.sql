-- to render real upcoming/total counts on the cards. A view runs with the
-- privileges of its owner, so this SELECT grant is what exposes the aggregate
-- without ever granting anon direct SELECT on the underlying events rows.
GRANT SELECT ON public.chapter_stats TO anon;

