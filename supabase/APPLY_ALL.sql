-- 4. BACKFILL events.chapter_id from events.city.
--
-- events.city is kebab-cased in prod, so the primary join is on the normalized
-- key. We normalize lower(trim(city)) defensively (in case a future anon
-- submit-event row arrives mixed-case or padded) and apply the SAME alias
-- folding js/clawcamp-map.js uses, so the spaced 'san francisco' and the bare
-- 'sf' both collapse onto the single 'sf' chapter — there is NO second SF
-- bucket. Rows whose normalized city does not match any seeded chapter are
-- LEFT chapter_id = NULL on purpose (exit criterion: null only for genuinely
-- unmatched rows), so a human can review them rather than mis-bucketing.
--
-- Done as a correlated UPDATE so it is order-independent and re-runnable:
-- re-applying only re-resolves the same key, it never reassigns a row to a
-- different chapter.
-- ---------------------------------------------------------------------------
UPDATE public.events AS e
SET chapter_id = c.id
FROM public.chapters AS c
WHERE c.slug = (
        CASE
          -- Fold every San Francisco spelling onto the single 'sf' chapter.
          WHEN lower(trim(e.city)) IN ('san-francisco', 'san francisco', 'sf')
            THEN 'sf'
          -- Treat blank/empty city as the online catch-all bucket.
          WHEN e.city IS NULL OR trim(e.city) = ''
            THEN 'online'
          -- Otherwise the kebab city key IS the chapter slug.
          ELSE lower(trim(e.city))
        END
      )
  -- Only set it where it is currently unresolved (idempotent / non-clobbering).
  AND e.chapter_id IS NULL;
-- NOTE: rows whose computed slug matches no seeded chapter simply don't join
-- here and keep chapter_id = NULL — exactly the "unmatched -> NULL" contract.

-- ---------------------------------------------------------------------------
-- 5. VIEW: chapter_stats  (public social-proof aggregate)
--
-- Replaces BOTH fake data sources on chapters/index.html:
--   * the hardcoded '5' upcoming / '3' captains strings baked into the cards, and
--   * the DEAD follower read (loadFollowers() hits contacts?form_type=eq.
--     chapter_follow, which returns nothing now that v1.0.0 REVOKEd anon SELECT
--     on contacts — see 0001).
-- Every number here is DERIVED FROM A REAL DB COUNT against events.chapter_id;
-- if a 'followers' figure is ever wanted it must likewise come from a real
-- count, NEVER from the killed anon contacts read.
--
-- Exposes one row per chapter: id, slug, upcoming_events, total_events.
-- LEFT JOIN so chapters with zero events still appear (counts = 0, not absent).
-- ---------------------------------------------------------------------------
CREATE OR REPLACE VIEW public.chapter_stats AS
SELECT
  c.id                                                        AS id,
  c.slug                                                      AS slug,
  -- upcoming = future-or-today events linked to this chapter
  count(e.id) FILTER (WHERE e.event_date >= current_date)     AS upcoming_events,
  -- total = all events ever linked to this chapter
  count(e.id)                                                 AS total_events
FROM public.chapters AS c
LEFT JOIN public.events AS e ON e.chapter_id = c.id
GROUP BY c.id, c.slug;

