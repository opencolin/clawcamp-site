-- ===========================================================================
-- ClawCamp — Migration 0002: Chapters table + events.chapter_id FK
-- ===========================================================================
-- WHY THIS FILE EXISTS:
--   v1.1.0 ("chapters-are-real") turns chapters from hardcoded HTML cards into
--   real DB rows. Today chapters/index.html ships exactly two hand-coded cards
--   (a "ClawCamp San Francisco" card and an "Online Events" catch-all), with
--   FAKE social-proof numbers baked into the markup ('5' upcoming, '3' captains)
--   and a follower count read from the now-DEAD anon `contacts` SELECT path
--   (loadFollowers() in chapters/index.html fetches contacts?form_type=eq.
--   chapter_follow — that read returns nothing post-0001, because v1.0.0
--   REVOKEd anon SELECT on contacts). This migration is the data spine every
--   other v1.1.0 slice depends on: it must be committed (and applied in the
--   hosted Supabase console by an admin — anon cannot run DDL) BEFORE the table
--   exists for the chapters page / scripts slices to consume.
--
-- WHAT THIS MIGRATION DOES (in order):
--   1. CREATE public.chapters  — the real chapter directory.
--   2. ALTER public.events ADD chapter_id bigint FK -> chapters(id).
--   3. Seed chapters (SF + Online catch-all + the distinct real cities that
--      already appear in events.city).
--   4. Backfill events.chapter_id from the (kebab-cased) events.city value,
--      leaving genuinely-unmatched rows NULL pending review.
--   5. CREATE public.chapter_stats — a public-readable aggregate view that
--      exposes REAL upcoming/total event counts (replaces the fake numbers).
--   6. RLS lockdown: anon may SELECT chapters (public cards) but NOT write.
--
-- THE FK-vs-SLUG DECISION (explicitly resolved — read before "simplifying"):
--   events.chapter_id is a BIGINT FK referencing chapters(id) — deliberately
--   NOT a slug/city string. The app today guesses a chapter by city-substring
--   matching, e.g. (city||'').indexOf('san francisco') >= 0 ||
--   (city||'').indexOf('sf') >= 0 in chapters/index.html loadFollowers(). That
--   substring approach silently double-counts and drifts (any city containing
--   the letters "sf"/"san francisco" matches). A hard integer FK kills that
--   drift: a row is in exactly one chapter or in none. The human-friendly slug
--   stays ONLY on chapters.slug as the stable URL key ('sf', 'online', ...).
--
-- DESIGN NOTES (conventions inherited from 0001 — kept consistent on purpose):
--   * id uses `bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY`, matching
--     the contacts/events PK style in 0001 (NOT bigserial, NOT uuid).
--   * `status` is free-text + a CHECK constraint (active|forming|archived),
--     NOT a Postgres ENUM type — this matches 0001's free-text+constraint
--     convention (e.g. contacts.form_type / events.event_type are plain text)
--     and avoids the migration pain of ALTER TYPE ... ADD VALUE later.
--   * events.city in prod is uniformly kebab-cased ('san-francisco', 'oakland',
--     'online', 'new-york', ...) — verified live via the anon events SELECT and
--     mirrored by the data-city attributes in index.html / events/index.html
--     and the alias map in js/clawcamp-map.js (cityNames: 'san francisco'->'
--     san-francisco', 'sf'->'san-francisco'). The backfill below joins on that
--     kebab key but ALSO normalizes lower(trim(...)) and folds the spaced 'san
--     francisco' / bare 'sf' spellings into the single SF chapter, so a future
--     free-text submit-event row cannot create a second SF bucket.
-- ===========================================================================

-- ---------------------------------------------------------------------------
-- 1. TABLE: chapters  (the real chapter directory; replaces hardcoded cards)
-- Column set is the v1.1.0 converged schema. Every column gets a WHAT/WHY.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.chapters (
  -- meta / identity
  id             bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- matches contacts/events PK style in 0001
  slug           text NOT NULL UNIQUE,   -- stable URL key, e.g. 'sf', 'online' — the ONLY place a string key lives
  name           text NOT NULL,          -- display name on the card, e.g. 'ClawCamp SF, CA'

  -- geography (nullable: the 'online' catch-all has no physical location)
  city           text,                   -- human city label for the card
  region         text,                   -- state / province / region
  country        text,                   -- ISO-ish country label, e.g. 'US'
  lat            double precision,        -- map pin latitude  (from js/clawcamp-map.js cityCoords)
  lng            double precision,        -- map pin longitude (from js/clawcamp-map.js cityCoords)

  -- card presentation
  emoji          text,                   -- card glyph, e.g. '🦞' / '🌐'
  cover_gradient text,                   -- CSS gradient string used as the card cover background
  blurb          text,                   -- short description shown in the card body

  -- lifecycle
  -- Free-text + CHECK (NOT a Postgres enum) to match the 0001 convention.
  status         text NOT NULL DEFAULT 'active'
                   CHECK (status IN ('active','forming','archived')),
  created_at     timestamptz NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- 2. FK column on events: events.chapter_id -> chapters.id
-- Integer FK (NOT a slug string) — see "THE FK-vs-SLUG DECISION" header note.
-- Nullable on purpose: events with no confident chapter match stay NULL and
-- are surfaced for human review rather than being force-bucketed.
-- ---------------------------------------------------------------------------
ALTER TABLE public.events
  ADD COLUMN IF NOT EXISTS chapter_id bigint REFERENCES public.chapters(id);

