      public.is_claw_admin()
      OR EXISTS (
        SELECT 1
          FROM public.events e
         WHERE e.id = rsvps.event_id
           AND e.chapter_id IS NOT NULL
           AND public.is_chapter_role(e.chapter_id, 'captain')
      )
    );
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

