      OR public.is_chapter_role(chapters.id, 'captain')
    )
    WITH CHECK (
      public.is_claw_admin()
      OR public.is_chapter_role(chapters.id, 'captain')
    );
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

-- ===========================================================================
-- 6. rsvps captain-SELECT policy  (roster read for the console + CSV)
-- ===========================================================================
-- WHAT: in addition to 0004's rsvps_select_own (a user sees their OWN rsvps),
--       add a SELECT policy letting a CAPTAIN read the rsvps roster (emails /
--       names) for events IN THEIR OWN CHAPTER only — or a global admin read
--       any roster. This powers the captain console's attendee roster + CSV
--       export.
-- WHY / PII REASONING: rsvp emails are PII (0004's threat model). 0004 gave anon
