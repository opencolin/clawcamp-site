-- ===========================================================================
-- LOCKDOWN POLICY STATEMENTS  (v1.0.0 breach fix — the auditable change)
-- ===========================================================================
-- Each statement is commented with WHAT it does and WHY. The combined effect:
-- anon can INSERT a contact (forms keep working) but can NO LONGER read or
-- overwrite contacts; authenticated users can read/update ONLY their own row,
-- matched by the email claim in their JWT.
-- ===========================================================================

-- Turn on row-level security so the policies below are actually enforced.
-- (Was DISABLED in the old policy — every grant was effectively wide open.)
ALTER TABLE public.contacts ENABLE ROW LEVEL SECURITY;

-- Revoke the anon SELECT grant: anon can no longer read ANY contact row,
-- which is what was leaking email/phone + verification_token/magic_link_token.
REVOKE SELECT ON public.contacts FROM anon;

-- Revoke the anon UPDATE grant: anon can no longer PATCH/overwrite contacts
-- (this is the "PATCH ?email=eq.<anyone>" account-takeover hole).
REVOKE UPDATE ON public.contacts FROM anon;

-- Keep anon INSERT so the public forms (signup, newsletter, all form_types)
-- continue to work. This is the ONLY contacts privilege anon retains.
GRANT INSERT ON public.contacts TO anon;

-- SELECT policy for the authenticated role: a logged-in user may read ONLY
-- the contact row whose `email` matches the email claim in their JWT. This
-- is what powers loadProfile()/loadPrefs() in the dashboard now that anon
-- SELECT is gone — the dashboard must call these with the user's access_token.
CREATE POLICY contacts_select_own
  ON public.contacts
  FOR SELECT
  TO authenticated
  USING (email = (auth.jwt() ->> 'email'));

-- UPDATE policy for the authenticated role: a logged-in user may update ONLY
-- their own row (matched on the JWT email), and may not relabel a row onto
-- someone else's email (WITH CHECK enforces the post-update email too). This
-- replaces the anon PATCH-by-email path used by saveProfile()/savePref().
CREATE POLICY contacts_update_own
  ON public.contacts
  FOR UPDATE
  TO authenticated
  USING (email = (auth.jwt() ->> 'email'))
  WITH CHECK (email = (auth.jwt() ->> 'email'));

