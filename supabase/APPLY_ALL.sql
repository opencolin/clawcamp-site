-- The public chapters page must READ chapter cards, so anon gets SELECT only.
-- anon is intentionally given NO INSERT/UPDATE/DELETE on chapters: chapter
-- creation goes through the existing 'chapter_application' form into contacts
-- and is promoted by an admin — never written directly by the browser. This
-- is the assertion target for the chapters rls-probe slice (clone of
-- scripts/rls-probe.sh): anon SELECT must succeed, anon writes must be denied.
-- ===========================================================================

-- Enforce row-level security on chapters so the SELECT policy below applies.
ALTER TABLE public.chapters ENABLE ROW LEVEL SECURITY;

-- Grant anon the SELECT privilege (table-level) so the public site can read
-- chapter cards. No INSERT/UPDATE/DELETE grant is issued to anon — writes stay
-- closed to the hostile public client.
GRANT SELECT ON public.chapters TO anon;

-- Row-level SELECT policy: every chapter row is public (USING true). Combined
-- with the GRANT above, anon can read all chapters but nothing more.
CREATE POLICY chapters_select_public
  ON public.chapters
  FOR SELECT
  TO anon
  USING (true);

